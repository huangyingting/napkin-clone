#!/usr/bin/env node

import { randomBytes } from "node:crypto";
import { existsSync, lstatSync, mkdirSync, writeFileSync } from "node:fs";
import { basename, join } from "node:path";
import process from "node:process";
import { pathToFileURL } from "node:url";

export function sanitizeWorktreeName(name) {
  const sanitized = name.toLowerCase().replace(/[^a-z0-9_-]+/g, "-");
  return sanitized.replace(/^-+|-+$/g, "") || "worktree";
}

export function buildWorktreeEnv({
  worktreeName,
  port = 4000,
  authSecret = randomBytes(32).toString("base64"),
}) {
  const name = sanitizeWorktreeName(worktreeName);
  return [
    "# Generated by npm run dev:worktree. Source this file for isolated local dev.",
    'DB_PROVIDER="sqlite"',
    `DATABASE_URL="file:./prisma/dev.${name}.db"`,
    `AUTH_SECRET="${authSecret}"`,
    `AUTH_URL="http://localhost:${port}"`,
    `NEXT_PUBLIC_APP_URL="http://localhost:${port}"`,
    "",
  ].join("\n");
}

export function inspectWorktree(repoRoot = process.cwd()) {
  const checks = [];
  checks.push(pathCheck(repoRoot, "node_modules", "dependency tree"));
  checks.push(pathCheck(repoRoot, "src/generated", "generated Prisma client"));
  checks.push(pathCheck(repoRoot, ".next", "Next build output"));
  checks.push(pathCheck(repoRoot, "storage", "local asset storage"));
  return checks;
}

export function worktreeInstructions({
  envFile = ".env.worktree",
  port = 4000,
} = {}) {
  return [
    `Run with isolated env: set -a; . ./${envFile}; set +a; npm run dev`,
    `Open http://localhost:${port}`,
    "If .next was produced against a different node_modules tree, remove it before rebuilding.",
    "If src/generated is missing, run npm run db:generate.",
  ];
}

function pathCheck(repoRoot, relativePath, label) {
  const fullPath = join(repoRoot, relativePath);
  const stat = lstatSync(fullPath, { throwIfNoEntry: false });
  if (!stat)
    return { path: relativePath, label, exists: false, symlink: false };
  return {
    path: relativePath,
    label,
    exists: true,
    symlink: stat.isSymbolicLink(),
  };
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const repoRoot = process.cwd();
  const worktreeName = sanitizeWorktreeName(basename(repoRoot));
  const envFile = ".env.worktree";
  const envPath = join(repoRoot, envFile);
  if (!existsSync(envPath)) {
    writeFileSync(envPath, buildWorktreeEnv({ worktreeName }), { mode: 0o600 });
    console.log(
      `Created ${envFile} with isolated SQLite path for ${worktreeName}.`,
    );
  } else {
    console.log(`${envFile} already exists; leaving it unchanged.`);
  }
  mkdirSync(join(repoRoot, "storage", "slide-assets"), { recursive: true });

  for (const check of inspectWorktree(repoRoot)) {
    const state = check.exists
      ? check.symlink
        ? "symlink"
        : "present"
      : "missing";
    console.log(`- ${check.path}: ${state} (${check.label})`);
    if (check.path === ".next" && check.exists) {
      console.log(
        "  Hint: remove .next before rebuilding after switching dependency trees.",
      );
    }
    if (check.path === "node_modules" && check.symlink) {
      console.log(
        "  Hint: keep .next worktree-local; do not share .next across symlinked node_modules worktrees.",
      );
    }
  }

  console.log("");
  for (const line of worktreeInstructions({ envFile })) {
    console.log(line);
  }
}

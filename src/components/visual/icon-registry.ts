/**
 * Resolves a catalog icon name to its lucide component, or `undefined` when the
 * name is not in the registry (so callers can gracefully render no icon). The
 * `hasOwnProperty` guard avoids matching inherited object keys (e.g. "toString").
 */
export function resolveIconComponent(
  name: string | null | undefined,
): LucideIcon | undefined {
  if (name == null) return undefined;
  return Object.prototype.hasOwnProperty.call(ICON_COMPONENTS, name)
    ? ICON_COMPONENTS[name]
    : undefined;
}
